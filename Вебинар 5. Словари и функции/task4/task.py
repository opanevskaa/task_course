def scrabble(word):
    """Игра 'Эрудит'
    :param word: слово
    :return: количество очков за слово
    """
    # Словарь, где ключ — это количество очков, а значение — список букв
    points_dict = {
        1: ['а', 'в', 'е', 'ё', 'и', 'н', 'о', 'р', 'с', 'т'],
        2: ['д', 'к', 'л', 'м', 'п', 'у'],
        3: ['б', 'г', 'ь', 'я'],
        4: ['й', 'ы'],
        5: ['ж', 'з', 'х', 'ц', 'ч'],
        8: ['ф', 'ш', 'э', 'ю'],
        10: ['щ'],
        15: ['ъ']
    }

    # Переводим слово в нижний регистр
    word = word.lower()

    # Счетчик очков
    total_points = 0

    # Подсчет очков за каждую букву в слове
    for letter in word:
        for points, letters in points_dict.items():
            if letter in letters:
                total_points += points
                break  # Если буква найдена, выходим из внутреннего цикла

    return total_points

# Пример использования
print(scrabble('курс'))  # Вывод: 6 (к=2, у=2, р=1, с=1)

